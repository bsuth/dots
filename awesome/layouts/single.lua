
--------------------------------------------------------------------------------
-- LAYOUT
--------------------------------------------------------------------------------

local single = {
	name = 'single',
}

--------------------------------------------------------------------------------
-- FUNCTIONS
--------------------------------------------------------------------------------

function single.arrange(p)
    local wa = p.workarea
    local cls = p.clients

    for i = 1, #cls do
        p.geometries[cls[i]] = wa
    end
end

--------------------------------------------------------------------------------
-- RETURN
--------------------------------------------------------------------------------

return single
